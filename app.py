        "import streamlit as st\n",
        "import csv\n",
        "import os\n",
        "import pandas as pd\n",
        "\n",
        "# ----------------- CSV Setup ----------------- #\n",
        "FILENAME = \"students.csv\"\n",
        "\n",
        "if not os.path.exists(FILENAME):\n",
        "    with open(FILENAME, mode='w', newline='') as file:\n",
        "        writer = csv.writer(file)\n",
        "        writer.writerow([\"VID\", \"Name\", \"Citizen\", \"Course\", \"Status\"])\n",
        "\n",
        "# ----------------- Functions ----------------- #\n",
        "\n",
        "def add_student(vid, name, citizen, course, status):\n",
        "    with open(FILENAME, mode='a', newline='') as file:\n",
        "        writer = csv.writer(file)\n",
        "        writer.writerow([vid, name, citizen, course, status])\n",
        "\n",
        "def read_student(vid):\n",
        "    df = pd.read_csv(FILENAME)\n",
        "    return df[df[\"VID\"] == vid]\n",
        "\n",
        "def update_student(vid, name, citizen, course, status):\n",
        "    df = pd.read_csv(FILENAME)\n",
        "    if vid in df[\"VID\"].values:\n",
        "        df.loc[df[\"VID\"] == vid, [\"Name\",\"Citizen\",\"Course\",\"Status\"]] = [\n",
        "            name, citizen, course, status\n",
        "        ]\n",
        "        df.to_csv(FILENAME, index=False)\n",
        "        return True\n",
        "    return False\n",
        "\n",
        "def delete_student(vid):\n",
        "    df = pd.read_csv(FILENAME)\n",
        "    if vid in df[\"VID\"].values:\n",
        "        df = df[df[\"VID\"] != vid]\n",
        "        df.to_csv(FILENAME, index=False)\n",
        "        return True\n",
        "    return False\n",
        "\n",
        "def show_all_students():\n",
        "    return pd.read_csv(FILENAME)\n",
        "\n",
        "# ----------------- Streamlit UI ----------------- #\n",
