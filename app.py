        "    course = st.text_input(\"Course\")\n",
        "    status = st.text_input(\"Status\")\n",
        "\n",
        "    if st.button(\"Add Student\"):\n",
        "        add_student(vid, name, citizen, course, status)\n",
        "        st.success(\"Student added successfully!\")\n",
        "\n",
        "# -------- View Student -------- #\n",
        "elif menu == \"View Student\":\n",
        "    st.subheader(\"üîç View Student Record\")\n",
        "    vid = st.number_input(\"Enter VID\", min_value=1)\n",
        "\n",
